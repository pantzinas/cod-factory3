package gr.aueb.cf.ch2;

/**
 * Demo for typecast
 */

public class TypeCastApp {
    public static void main(String[] args) {
        int num1 = 10; // int literals --> int -->32-bits
        long num2 = 20L; // we declare the literal 20 (which is an integer) as long with adding the L

        num1 = (int) num2;


    }



}
